-- Create main table
CREATE TABLE TRANSACTION_HISTORY (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    transaction_identity TEXT UNIQUE NOT NULL,
    epoch_milliseconds INTEGER NOT NULL,
    amount INTEGER NOT NULL,
    currency TEXT NOT NULL,
    currency_specifications TEXT,
    tranx_purpose TEXT,
    incoming INTEGER NOT NULL
);

-- Indexes
CREATE INDEX idx_amt ON TRANSACTION_HISTORY(amount);
CREATE INDEX idx_dtm ON TRANSACTION_HISTORY(epoch_milliseconds);

